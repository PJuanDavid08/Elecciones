    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
      
        String nombre = this.campoNombre.getText();
        String correo = this.comboCategoria.getSelectedItem().toString();
        String fechaInicio = this.campoFechaInicio.getDateFormatString();
        String fechaFin = this.campoFechaFin.getDateFormatString();

        ClsEleccion eleccion = new ClsEleccion(fechaFin, fechaFin, fechaInicio, fechaFin, correo);
        
        ClsMensaje mensaje = this.controlador.actualizarEleccion(eleccion);
       
       if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            obtenerElecciones();
      }
       JOptionPane.showMessageDialog(rootPane, mensaje.getTexto());
        
    }//GEN-LAST:event_botonActualizarActionPerformed

