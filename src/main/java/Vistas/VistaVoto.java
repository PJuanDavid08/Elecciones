   private void obtenerResultados(){
    
        this.listaResultados = this.controladorVoto.obtenerResultados();
        this.actualizarTabla(listaResultados);
    }
    
       public void actualizarTabla(LinkedList<ClsVoto> voto){
        
        DefaultTableModel modelo = (DefaultTableModel)this.tablaResultados.getModel();
        modelo.setRowCount(0);
        for (ClsVoto c : voto) {
        
            Object[] fila = {c.getIdVoto(), c.getIdEleccion(), 
                c.getIdCandidato(), c.getIdVotante()};
            
            modelo.addRow(fila);
        }
        
    }
   
    
