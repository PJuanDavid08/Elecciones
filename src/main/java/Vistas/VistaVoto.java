    private void botonVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVotarActionPerformed
       
        String idVoto = this.campoCodigo.getText();
        String idVotante = this.campoIdVotante.getText();
        String idEleeccion =this.comboEleccion.getSelectedItem().toString();
        String idCandidato =this.comboCandidato.getSelectedItem().toString();
        
        ClsVoto voto = new ClsVoto(idVoto, idEleeccion, idCandidato, idVotante);
        
        ClsMensaje mensaje = this.controladorVoto.agregarVoto(voto);
        if (mensaje.getTipo().equals(ClsMensaje.OK)) {
            obtenerResultados();
      }
       JOptionPane.showMessageDialog(rootPane, mensaje.getTexto());
    
    }//GEN-LAST:event_botonVotarActionPerformed

